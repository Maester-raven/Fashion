#!/usr/bin/env python3
"""Evaluate COCO instance predictions with PRD-style metrics.

The script expects a COCO GT annotation JSON and a COCO-style prediction
`results.segm.json` generated by MMDetection. It reports:

- Recall = same-class matched predictions / GT instances
- Precision-like = same-class matched predictions / output predictions
- Mean TP IoU = mean mask IoU of matched true positives

It also supports the current RTMDet post-processing policy:

- class-specific score thresholds
- same-class duplicate resolver
- pants/skirt/dress hard conflict resolver
- optional accessory-contained-by-clothing filter
"""

from __future__ import annotations

import argparse
import json
from collections import Counter, defaultdict
from pathlib import Path
from typing import Any

import numpy as np
from pycocotools import mask as mask_utils


DEFAULT_THRESHOLDS = {
    "top": 0.35,
    "pants": 0.35,
    "skirt": 0.43,
    "outerwear": 0.40,
    "dress": 0.35,
    "shoes": 0.35,
    "bag": 0.40,
    "accessory": 0.38,
}


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser()
    parser.add_argument("--gt-json", required=True)
    parser.add_argument("--pred-segm-json", required=True)
    parser.add_argument("--output-json", required=True)
    parser.add_argument("--min-score", type=float, default=0.25)
    parser.add_argument("--match-iou", type=float, default=0.05)
    parser.add_argument(
        "--thresholds",
        default=None,
        help=(
            "Comma-separated class thresholds, e.g. "
            "top=0.35,pants=0.35,skirt=0.43. Defaults to current best policy."
        ),
    )
    parser.add_argument(
        "--resolver",
        choices=["none", "prd_best"],
        default="prd_best",
        help="prd_best applies same-class + pants/skirt/dress conflict resolver.",
    )
    parser.add_argument("--mask-iou-thr", type=float, default=0.30)
    parser.add_argument("--same-contain-thr", type=float, default=0.85)
    parser.add_argument("--cross-contain-thr", type=float, default=0.92)
    parser.add_argument("--skirt-score-penalty", type=float, default=0.85)
    parser.add_argument("--pants-score-bonus", type=float, default=1.06)
    parser.add_argument("--dress-score-bonus", type=float, default=1.00)
    parser.add_argument("--accessory-cloth-contain-thr", type=float, default=0.98)
    parser.add_argument("--accessory-filter-score-max", type=float, default=0.60)
    return parser.parse_args()


def normalize_rle(seg: Any, height: int, width: int) -> dict[str, Any]:
    if isinstance(seg, list):
        rle = mask_utils.merge(mask_utils.frPyObjects(seg, height, width))
    else:
        rle = dict(seg)
    if isinstance(rle.get("counts"), str):
        rle["counts"] = rle["counts"].encode("ascii")
    return rle


def bbox_intersection(a: list[float], b: list[float]) -> float:
    ax, ay, aw, ah = a
    bx, by, bw, bh = b
    iw = max(0.0, min(ax + aw, bx + bw) - max(ax, bx))
    ih = max(0.0, min(ay + ah, by + bh) - max(ay, by))
    return iw * ih


def bbox_iou(a: list[float], b: list[float]) -> float:
    inter = bbox_intersection(a, b)
    if inter <= 0:
        return 0.0
    return inter / max(1e-9, a[2] * a[3] + b[2] * b[3] - inter)


def parse_thresholds(raw: str | None, name_to_id: dict[str, int]) -> dict[int, float]:
    values = dict(DEFAULT_THRESHOLDS)
    if raw:
        for item in raw.split(","):
            if not item.strip():
                continue
            key, value = item.split("=", 1)
            values[key.strip()] = float(value)
    return {name_to_id[name]: value for name, value in values.items() if name in name_to_id}


class Evaluator:
    def __init__(self, gt_json: dict[str, Any], pred_json: list[dict[str, Any]], args: argparse.Namespace):
        self.args = args
        self.cat_id_to_name = {c["id"]: c["name"] for c in gt_json["categories"]}
        self.name_to_cat_id = {v: k for k, v in self.cat_id_to_name.items()}
        self.cat_ids = sorted(self.cat_id_to_name)
        self.thresholds = parse_thresholds(args.thresholds, self.name_to_cat_id)
        self.image_hw = {im["id"]: (im["height"], im["width"]) for im in gt_json["images"]}
        self.mask_cache: dict[int, np.ndarray] = {}

        self.gt_by_img_cat: dict[int, dict[int, list[dict[str, Any]]]] = defaultdict(lambda: defaultdict(list))
        self.gt_count = Counter()
        for ann in gt_json["annotations"]:
            height, width = self.image_hw[ann["image_id"]]
            rle = normalize_rle(ann["segmentation"], height, width)
            x, y, bw, bh = ann.get("bbox", [0, 0, 0, 0])
            gt = {
                "rle": rle,
                "bbox": [float(x), float(y), float(bw), float(bh)],
                "area": float(mask_utils.area(rle)),
            }
            self.gt_by_img_cat[ann["image_id"]][ann["category_id"]].append(gt)
            self.gt_count[ann["category_id"]] += 1

        self.pred_by_img: dict[int, list[dict[str, Any]]] = defaultdict(list)
        for idx, pred in enumerate(pred_json):
            score = float(pred.get("score", 0.0))
            if score < args.min_score:
                continue
            rle = dict(pred["segmentation"])
            if isinstance(rle.get("counts"), str):
                rle["counts"] = rle["counts"].encode("ascii")
            x, y, bw, bh = pred.get("bbox", [0, 0, 0, 0])
            item = {
                "idx": idx,
                "image_id": pred["image_id"],
                "category_id": pred["category_id"],
                "score": score,
                "rle": rle,
                "bbox": [float(x), float(y), float(bw), float(bh)],
                "area": float(mask_utils.area(rle)),
            }
            self.pred_by_img[pred["image_id"]].append(item)
        for image_id in self.pred_by_img:
            self.pred_by_img[image_id].sort(key=lambda p: p["score"], reverse=True)

    def mask(self, pred: dict[str, Any]) -> np.ndarray:
        idx = pred["idx"]
        if idx not in self.mask_cache:
            self.mask_cache[idx] = mask_utils.decode(pred["rle"]).astype(bool)
        return self.mask_cache[idx]

    def mask_pair_stats(self, a: dict[str, Any], b: dict[str, Any]) -> tuple[float, float]:
        am = self.mask(a)
        bm = self.mask(b)
        inter = np.logical_and(am, bm).sum()
        if inter <= 0:
            return 0.0, 0.0
        union = np.logical_or(am, bm).sum()
        mask_iou = inter / max(1, union)
        contain = inter / max(1.0, min(a["area"], b["area"]))
        return float(mask_iou), float(contain)

    def sort_score(self, pred: dict[str, Any]) -> float:
        name = self.cat_id_to_name[pred["category_id"]]
        score = pred["score"]
        if name == "skirt":
            score *= self.args.skirt_score_penalty
        elif name == "pants":
            score *= self.args.pants_score_bonus
        elif name == "dress":
            score *= self.args.dress_score_bonus
        return score

    def is_hard_conflict(self, a_name: str, b_name: str) -> bool:
        if a_name == b_name:
            return True
        return frozenset([a_name, b_name]) in {
            frozenset(["pants", "skirt"]),
            frozenset(["pants", "dress"]),
            frozenset(["skirt", "dress"]),
        }

    def filter_predictions(self) -> dict[int, list[dict[str, Any]]]:
        output = defaultdict(list)
        clothing = {"top", "pants", "skirt", "outerwear", "dress"}
        for image_id, preds in self.pred_by_img.items():
            candidates = [
                pred
                for pred in preds
                if pred["score"] >= self.thresholds.get(pred["category_id"], self.args.min_score)
            ]
            if self.args.resolver == "none":
                output[image_id] = candidates
                continue

            kept: list[dict[str, Any]] = []
            for pred in sorted(candidates, key=self.sort_score, reverse=True):
                pred_name = self.cat_id_to_name[pred["category_id"]]
                drop = False
                for kept_pred in kept:
                    kept_name = self.cat_id_to_name[kept_pred["category_id"]]
                    if not self.is_hard_conflict(pred_name, kept_name):
                        continue
                    inter = bbox_intersection(pred["bbox"], kept_pred["bbox"])
                    if inter <= 0:
                        continue
                    box_contain = inter / max(1e-9, min(pred["bbox"][2] * pred["bbox"][3], kept_pred["bbox"][2] * kept_pred["bbox"][3]))
                    if bbox_iou(pred["bbox"], kept_pred["bbox"]) < 0.10 and box_contain < 0.55:
                        continue
                    mask_iou, contain = self.mask_pair_stats(pred, kept_pred)
                    contain_thr = self.args.same_contain_thr if pred_name == kept_name else self.args.cross_contain_thr
                    if mask_iou >= self.args.mask_iou_thr or contain >= contain_thr:
                        drop = True
                        break
                if not drop:
                    kept.append(pred)

            if self.args.accessory_cloth_contain_thr > 0:
                clothes = [p for p in kept if self.cat_id_to_name[p["category_id"]] in clothing]
                filtered = []
                for pred in kept:
                    pred_name = self.cat_id_to_name[pred["category_id"]]
                    if pred_name != "accessory" or pred["score"] > self.args.accessory_filter_score_max:
                        filtered.append(pred)
                        continue
                    contained = False
                    for cloth in clothes:
                        if bbox_intersection(pred["bbox"], cloth["bbox"]) <= 0:
                            continue
                        _, contain = self.mask_pair_stats(pred, cloth)
                        if contain >= self.args.accessory_cloth_contain_thr:
                            contained = True
                            break
                    if not contained:
                        filtered.append(pred)
                kept = filtered
            output[image_id] = kept
        return output

    def evaluate(self) -> dict[str, Any]:
        filtered = self.filter_predictions()
        tp = fp = fn = 0
        ious = []
        per_class = {
            cid: {"gt": self.gt_count[cid], "pred": 0, "tp": 0, "fp": 0, "fn": 0, "ious": []}
            for cid in self.cat_ids
        }
        image_ids = set(self.gt_by_img_cat) | set(filtered)
        for image_id in image_ids:
            preds_by_cat = defaultdict(list)
            for pred in filtered.get(image_id, []):
                preds_by_cat[pred["category_id"]].append(pred)
                per_class[pred["category_id"]]["pred"] += 1
            for cid in self.cat_ids:
                preds = preds_by_cat.get(cid, [])
                gts = self.gt_by_img_cat[image_id].get(cid, [])
                if not gts:
                    fp += len(preds)
                    per_class[cid]["fp"] += len(preds)
                    continue
                if not preds:
                    fn += len(gts)
                    per_class[cid]["fn"] += len(gts)
                    continue
                matrix = mask_utils.iou(
                    [p["rle"] for p in preds],
                    [g["rle"] for g in gts],
                    [False] * len(gts),
                )
                used_gt = set()
                for pred_idx, pred in sorted(enumerate(preds), key=lambda x: x[1]["score"], reverse=True):
                    best_gt = -1
                    best_iou = 0.0
                    for gt_idx in range(len(gts)):
                        if gt_idx in used_gt:
                            continue
                        value = float(matrix[pred_idx, gt_idx]) if matrix.size else 0.0
                        if value > best_iou:
                            best_iou = value
                            best_gt = gt_idx
                    if best_gt >= 0 and best_iou >= self.args.match_iou:
                        used_gt.add(best_gt)
                        tp += 1
                        per_class[cid]["tp"] += 1
                        ious.append(best_iou)
                        per_class[cid]["ious"].append(best_iou)
                    else:
                        fp += 1
                        per_class[cid]["fp"] += 1
                missed = len(gts) - len(used_gt)
                fn += missed
                per_class[cid]["fn"] += missed

        pred_total = tp + fp
        gt_total = tp + fn
        result = {
            "settings": {
                "thresholds": {self.cat_id_to_name[k]: v for k, v in self.thresholds.items()},
                "resolver": self.args.resolver,
                "mask_iou_thr": self.args.mask_iou_thr,
                "same_contain_thr": self.args.same_contain_thr,
                "cross_contain_thr": self.args.cross_contain_thr,
                "skirt_score_penalty": self.args.skirt_score_penalty,
                "pants_score_bonus": self.args.pants_score_bonus,
                "dress_score_bonus": self.args.dress_score_bonus,
                "accessory_cloth_contain_thr": self.args.accessory_cloth_contain_thr,
                "accessory_filter_score_max": self.args.accessory_filter_score_max,
            },
            "totals": {
                "gt": gt_total,
                "pred": pred_total,
                "tp": tp,
                "fp": fp,
                "fn": fn,
                "recall": tp / max(1, gt_total),
                "precision_like": tp / max(1, pred_total),
                "mean_tp_iou": float(np.mean(ious)) if ious else 0.0,
            },
            "per_class": {},
        }
        for cid, data in per_class.items():
            class_ious = data.pop("ious")
            result["per_class"][self.cat_id_to_name[cid]] = {
                **data,
                "recall": data["tp"] / max(1, data["gt"]),
                "precision_like": data["tp"] / max(1, data["pred"]),
                "mean_tp_iou": float(np.mean(class_ious)) if class_ious else 0.0,
            }
        return result


def main() -> None:
    args = parse_args()
    with open(args.gt_json, "r", encoding="utf-8") as f:
        gt_json = json.load(f)
    with open(args.pred_segm_json, "r", encoding="utf-8") as f:
        pred_json = json.load(f)
    result = Evaluator(gt_json, pred_json, args).evaluate()
    Path(args.output_json).parent.mkdir(parents=True, exist_ok=True)
    with open(args.output_json, "w", encoding="utf-8") as f:
        json.dump(result, f, ensure_ascii=False, indent=2)

    totals = result["totals"]
    print(
        "GT={gt} Pred={pred} TP={tp} FP={fp} FN={fn} "
        "Recall={recall:.4f} Precision-like={precision_like:.4f} "
        "MeanTP-IoU={mean_tp_iou:.4f}".format(**totals)
    )
    for name, item in result["per_class"].items():
        print(
            f"{name:<10} gt={item['gt']:>4} pred={item['pred']:>4} "
            f"tp={item['tp']:>4} fp={item['fp']:>4} fn={item['fn']:>4} "
            f"R={item['recall']*100:>6.2f} "
            f"P={item['precision_like']*100:>6.2f} "
            f"IoU={item['mean_tp_iou']*100:>6.2f}"
        )


if __name__ == "__main__":
    main()
